# Operador de adição
soma = 5 + 3
print(f'5 + 3 = {soma}')  # Saída: 8

# Operador de subtração
subtracao = 5 - 3
print(f'5 - 3 = {subtracao}')  # Saída: 2

# Operador de multiplicação
multiplicacao = 5 * 3
print(f'5 * 3 = {multiplicacao}')  # Saída: 15

# Operador de divisão
divisao = 5 / 3
print(f'5 / 3 = {divisao:.2f}')  # Saída: 1.67

# Operador de divisão inteira
divisao_inteira = 5 // 3
print(f'5 // 3 = {divisao_inteira}')  # Saída: 1

# Operador de módulo
modulo = 5 % 3
print(f'5 % 3 = {modulo}')  # Saída: 2

# Operador de exponenciação
exponenciacao = 5 ** 3
print(f'5 ** 3 = {exponenciacao}')  # Saída: 125

# Operador de igualdade
igual = 5 == 3
print(f'5 == 3 é {igual}')  # Saída: False

# Operador de diferença
diferente = 5 != 3
print(f'5 != 3 é {diferente}')  # Saída: True

# Operador de maior que
maior = 5 > 3
print(f'5 > 3 é {maior}')  # Saída: True

# Operador de menor que
menor = 5 < 3
print(f'5 < 3 é {menor}')  # Saída: False

# Operador de maior ou igual que
maior_ou_igual = 5 >= 3
print(f'5 >= 3 é {maior_ou_igual}')  # Saída: True

# Operador de menor ou igual que
menor_ou_igual = 5 <= 3
print(f'5 <= 3 é {menor_ou_igual}')  # Saída: False

# Operador lógico AND
e_logico = (5 > 3) and (3 > 1)
print(f'(5 > 3) e (3 > 1) é {e_logico}')  # Saída: True

# Operador lógico OR
ou_logico = (5 > 3) or (3 < 1)
print(f'(5 > 3) ou (3 < 1) é {ou_logico}')  # Saída: True

# Operador lógico NOT
nao_logico = not (5 > 3)
print(f'Não (5 > 3) é {nao_logico}')  # Saída: False

a = 5
print(f'a = {a}')  # Saída: 5

a += 3  # a = a + 3
print(f'a += 3 é {a}')  # Saída: 8

a -= 3  # a = a - 3
print(f'a -= 3 é {a}')  # Saída: 5

a *= 3  # a = a * 3
print(f'a *= 3 é {a}')  # Saída: 15

a /= 3  # a = a / 3
print(f'a /= 3 é {a:.2f}')  # Saída: 5.00

a %= 3  # a = a % 3
print(f'a %= 3 é {a}')  # Saída: 2

a //= 3  # a = a // 3
print(f'a //= 3 é {a}')  # Saída: 0

a **= 3  # a = a ** 3
print(f'a **= 3 é {a}')  # Saída: 0

# Operador AND bit a bit
bit_and = 5 & 3
print(f'5 & 3 é {bit_and}')  # Saída: 1

# Operador OR bit a bit
bit_or = 5 | 3
print(f'5 | 3 é {bit_or}')  # Saída: 7

# Operador XOR bit a bit
bit_xor = 5 ^ 3
print(f'5 ^ 3 é {bit_xor}')  # Saída: 6

# Operador NOT bit a bit
bit_not = ~5
print(f'~5 é {bit_not}')  # Saída: -6

# Operador deslocamento à esquerda
deslocamento_esquerda = 5 << 1
print(f'5 << 1 é {deslocamento_esquerda}')  # Saída: 10

# Operador deslocamento à direita
deslocamento_direita = 5 >> 1
print(f'5 >> 1 é {deslocamento_direita}')  # Saída: 2
