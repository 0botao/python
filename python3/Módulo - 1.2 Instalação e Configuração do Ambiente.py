# Antes de começar a programar em Python, precisamos instalar e configurar o ambiente de desenvolvimento.

# 1. Instalação do Python
# - Visite o site oficial do Python: https://www.python.org/downloads/
# - Baixe a versão mais recente do Python para o seu sistema operacional (Windows, macOS ou Linux).
# - Durante a instalação, marque a opção "Add Python to PATH" (Adicionar Python ao PATH) para facilitar o uso do Python a partir do terminal.

# 2. Verificação da Instalação
# - Abra o terminal (ou prompt de comando no Windows).
# - Execute o comando `python --version` para verificar se o Python foi instalado corretamente.
# - Você deve ver a versão do Python instalada, por exemplo: Python 3.9.1

# 3. Configuração do Ambiente de Desenvolvimento
# - Recomendamos o uso de um ambiente de desenvolvimento integrado (IDE) para facilitar a escrita e execução do código.
# - Algumas opções populares de IDEs são:
#     - PyCharm: https://www.jetbrains.com/pycharm/
#     - Visual Studio Code: https://code.visualstudio.com/
#     - Jupyter Notebook: https://jupyter.org/

# 4. Instalação de um IDE
# - Escolha o IDE de sua preferência e faça o download do instalador a partir do site oficial.
# - Siga as instruções de instalação fornecidas pelo site do IDE.
# - Após a instalação, abra o IDE e crie um novo projeto Python.

# 5. Testando o Ambiente de Desenvolvimento
# - Crie um novo arquivo Python no IDE com a extensão `.py`, por exemplo, `ola_mundo.py`.
# - Escreva o seguinte código no arquivo:
def ola_mundo():
    print("Olá, mundo!")

ola_mundo()

# - Execute o arquivo para garantir que tudo está configurado corretamente.
# - Você deve ver a mensagem "Olá, mundo!" impressa no console do IDE.

# Parabéns! Agora você tem o Python instalado e configurado, e está pronto para começar a programar.
