# O que é Python?
# Python é uma linguagem de programação de alto nível, interpretada e de propósito geral.
# Foi criada por Guido van Rossum e lançada pela primeira vez em 1991.
# É conhecida por sua sintaxe clara e legível, o que a torna uma excelente escolha para iniciantes e profissionais.

# História do Python:
# - 1991: Lançamento do Python 0.9.0
# - 2000: Lançamento do Python 2.0, introduzindo novos recursos como coleta de lixo e suporte completo a Unicode.
# - 2008: Lançamento do Python 3.0, trazendo várias melhorias e mudanças, mas não compatível com a versão 2.x.

# Por que usar Python?
# - Simplicidade e legibilidade do código.
# - Grande comunidade e vasto ecossistema de bibliotecas e frameworks.
# - Multiplataforma: funciona em Windows, macOS, Linux, etc.
# - Aplicações diversas: desenvolvimento web, análise de dados, automação, inteligência artificial, e muito mais.

# Aplicações do Python:
# - Desenvolvimento Web: frameworks como Django e Flask.
# - Análise de Dados: bibliotecas como Pandas, NumPy e Matplotlib.
# - Machine Learning e IA: bibliotecas como TensorFlow, Keras e scikit-learn.
# - Automação: scripts para automatizar tarefas repetitivas.
# - Desenvolvimento de Jogos: bibliotecas como Pygame.
# - Ciência e Pesquisa: utilizado em bioinformática, astronomia, entre outros campos.

